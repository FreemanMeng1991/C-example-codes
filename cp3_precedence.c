#include <stdio.h>
int main()
{
	int a=10,b=1,c=2,i=1,f;
	float d;

	//同一优先级的结合方向
	a=b=c; //向右结合a=(b=c)
	d = 12.0+4.0/5*2; ////乘除高于加减,4.0/5*2向左结合


	printf( "12+3*5=%f\n", d); 
	printf( "3>1+4 =%d\n", 3>1+4);    //数学运算高于关系运算
	printf( "3<4&&3==1 = %d\n", 3<4&&3==1); //关系运算高于逻辑运算
	printf( "i=1, ++i+ i++ + ++i + i = %d\n",  ++i+ i++ + ++i + i); //关系运算高于逻辑运算
	return 0;
}